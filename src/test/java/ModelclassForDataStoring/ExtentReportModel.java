package ModelclassForDataStoring;

public class ExtentReportModel {
String TestName;
String PassMessage;
String FailMessage;
String InfoMessage;
String WarningMessage;
String SkipMessage;
String Screenshot;
/**
 * @return the testName
 */
public String getTestName() {
	return TestName;
}
/**
 * @param testName the testName to set
 */
public void setTestName(String testName) {
	TestName = testName;
}
/**
 * @return the passMessage
 */
public String getPassMessage() {
	return PassMessage;
}
/**
 * @param passMessage the passMessage to set
 */
public void setPassMessage(String passMessage) {
	PassMessage = passMessage;
}
/**
 * @return the failMessage
 */
public String getFailMessage() {
	return FailMessage;
}
/**
 * @param failMessage the failMessage to set
 */
public void setFailMessage(String failMessage) {
	FailMessage = failMessage;
}
/**
 * @return the infoMessage
 */
public String getInfoMessage() {
	return InfoMessage;
}
/**
 * @param infoMessage the infoMessage to set
 */
public void setInfoMessage(String infoMessage) {
	InfoMessage = infoMessage;
}
/**
 * @return the warningMessage
 */
public String getWarningMessage() {
	return WarningMessage;
}
/**
 * @param warningMessage the warningMessage to set
 */
public void setWarningMessage(String warningMessage) {
	WarningMessage = warningMessage;
}
/**
 * @return the skipMessage
 */
public String getSkipMessage() {
	return SkipMessage;
}
/**
 * @param skipMessage the skipMessage to set
 */
public void setSkipMessage(String skipMessage) {
	SkipMessage = skipMessage;
}
/**
 * @return the screenshot
 */
public String getScreenshot() {
	return Screenshot;
}
/**
 * @param screenshot the screenshot to set
 */
public void setScreenshot(String screenshot) {
	Screenshot = screenshot;
}
}
